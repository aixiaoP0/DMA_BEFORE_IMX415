/**
 * H.264 测试数据
 *
 * 使用 ffmpeg 生成的 64x48 红色帧 IDR 关键帧，用于单元测试。
 * 生成命令：
 * ffmpeg -f lavfi -i color=c=red:s=64x48:r=1 -frames:v 1 -c:v libx264
 *        -preset ultrafast -tune zerolatency
 *        -x264-params keyint=1:min-keyint=1:scenecut=0
 *        -pix_fmt yuv420p -bsf:v filter_units=remove_types=6 -f h264
 */

/** Annex B 格式的 IDR 访问单元（含 SPS、PPS、IDR NAL） */
static const std::uint8_t kEmbeddedH264IdrAccessUnit[] = {
        0x00, 0x00, 0x00, 0x01, 0x67, 0x42, 0xC0, 0x0A, 0xDC, 0x47, 0xB0, 0x11,
        0x00, 0x00, 0x03, 0x00, 0x01, 0x00, 0x00, 0x03, 0x00, 0x02, 0x8F, 0x12,
        0x27, 0x80, 0x00, 0x00, 0x00, 0x01, 0x68, 0xCE, 0x0F, 0xC8, 0x00, 0x00,
        0x01, 0x65, 0x88, 0x84, 0x3A, 0x11, 0x8A, 0x00, 0x02, 0x18, 0xF1, 0xC0,
        0x00, 0x40, 0xF6, 0x38, 0x00, 0x08, 0x79, 0x49, 0xC9, 0xC9, 0xD7, 0x5D,
        0x75, 0xD7, 0x5D, 0x75, 0xE0,
};

static const std::size_t kEmbeddedH264IdrAccessUnitSize = sizeof(kEmbeddedH264IdrAccessUnit);

/** 将 IDR 帧拆分为独立的 NAL 单元列表（SPS、PPS、IDR） */
inline std::vector<std::vector<std::uint8_t> > EmbeddedH264IdrFrameNalus() {
    std::vector<std::vector<std::uint8_t> > nalus;
    // SPS (Sequence Parameter Set)
    nalus.push_back(std::vector<std::uint8_t>({
            0x67, 0x42, 0xC0, 0x0A, 0xDC, 0x47, 0xB0, 0x11, 0x00, 0x00, 0x03,
            0x00, 0x01, 0x00, 0x00, 0x03, 0x00, 0x02, 0x8F, 0x12, 0x27, 0x80,
    }));
    // PPS (Picture Parameter Set)
    nalus.push_back(std::vector<std::uint8_t>({0x68, 0xCE, 0x0F, 0xC8}));
    // IDR Slice
    nalus.push_back(std::vector<std::uint8_t>({
            0x65, 0x88, 0x84, 0x3A, 0x11, 0x8A, 0x00, 0x02, 0x18, 0xF1,
            0xC0, 0x00, 0x40, 0xF6, 0x38, 0x00, 0x08, 0x79, 0x49, 0xC9,
            0xC9, 0xD7, 0x5D, 0x75, 0xD7, 0x5D, 0x75, 0xE0,
    }));
    return nalus;
}

